<h4>Arashiyama Area</h4>
<p>A trip of about 30 minutes by train and foot leads to this area, with some of Kyoto's best sightseeing spots. Here you can enjoy sights including cherry blossoms in the spring, brilliant autumn colors, and the fantastic Bamboo Grove. The Togetsukyo bridge offers a great view of the beautiful scenery of each season. The Arashiyama area hosts not only nature, but also many World Heritage sites and cultural landmarks such as Tenryu-ji, famous for the paintings "Daruma-zu" and "Unryu-zu."
