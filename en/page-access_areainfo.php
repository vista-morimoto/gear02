<h4>Higashiyama Area</h4>
<p>Located about a 20-minute walk from the theater, you can enjoy a promenade through Gion. Gion is Kyoto's foremost geisha district, having developed as the street outside the gate of Yasaka Shrine, itself famous for its role in the Gion Festival. Gion is also increasingly popular as a sightseeing area. Walk a little further, and you can see Kiyomizu-dera, the most-visited World Heritage site in Kyoto, as well as Chion-in Temple. Chion-in is famous for the Sanmon gate, the largest structure of its kind in Japan, and the greatest surviving example of Japanese wood architecture. Even if you only have a short time, you can get have a full experience of Kyoto's unique sights.</p>
