<h3>Keito Kohara<br><span>Director</span></h3>
<p>
After experiencing a wide range of sites, from traveling shows by Kanji Fujiyama and Chocho Miyako to the Takarazuka Revue and Shiki Theatre Company to kabuki performances as a technical staff member, gained experience in Europe, mostly in Paris, as the technical director of Susan Buirge, the mother of contemporary dance, for about 7 years. Then, founded "Art Complex 1928" in 1999 as an effort to modernize the architecture of the former Mainichi Newspaper Kyoto branch and transitioned to the role of producer. With a theme of the complexity of art, in addition to theater production, continued activities related to the preparation of the artistic environment, such as establishment of a culture support fund and regeneration of shipyard ruins into "Creative Center Osaka." Also continues to start up various projects connecting cultural arts to the attraction of guests and revitalization of the city. In Kyoto, accomplished the 1,000th performance in June 2015 of "GEAR," the first long running content of its kind in Japan, and succeeded in exporting Japanese theater content overseas for the first time in November of the same year.
