<h2 class="GothicTitle">Greeting from the director of Gear</h2>
<p class="textBody">
I believe that the essence of Japanese culture is based on the idea of harmony within an animistic conception of the world. The true richness that we seek can be found in the tapestry that transcends the monolithic economic value system and results from an exquisitely balanced interweaving of things that are diverse and contrasting.<br>
<br>
When gears of different sizes, colors and shapes are put together, they link to produce a new harmony, within which true beauty emerges.<br>
<br>
This production is an attempt to find new possibilities for a small theater and expand both the range of theatergoing opportunities and the spectrum of the population that enjoys them. It also proposes a way to create new attractions in the area and make the community more appealing to visitors.<br>
<br>
Together, we, the cast and staff, devote ourselves throughout the year to deliver a quality production for all to enjoy, regardless of age, gender, nationality and language spoken, so that as many people as possible can experience the stimulation of their five senses, and the emotions and sense of unity made possible only through live performance.<br>
<br>
We hope that you will enjoy the show.
</p>

