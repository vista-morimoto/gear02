<p>The "GEAR" theatre is located in the 1928 Building, which was designed by architect Goichi Takeda and constructed in the year Showa 3 (1928) as the Kyoto bureau of the Mainichi Newspaper.<br>
As historical buildings continue to disappear, there were fears the deteriorating structure would be demolished. However, architect Hiroyuki Kawabayashi purchased  the property and had the walls reinforced to be earthquake resistant, thereby saving the building.<br>
The 1928 Building has remained in its original form for the past 90 years, accumulating a unique cultural ambience as time goes on.<br>
Inside, the arched main hall has provided great public enjoyment as a space for theatrical performances and speaking engagements since its initial completion.<br>
In 1999, new equipment was installed to meet the diverse needs of the modern era, enabling the space to host a wide range of events encompassing drama, music, and dance. It subsequently reopened under the name "Art Complex 1928." <br>
Since April of 2012 until the present, the building has served as the permanent home of the long-running theatrical production of "GEAR."<br>
We welcome audience members of "GEAR" to also enjoy the vintage atmosphere of our venue.
